function sendIqamaNotification(prayerName, minsBefore){
    if(Notification.permission === "granted"){
        new Notification("WAQSH Reminder", {
            body: `Iqamah for ${prayerName} in ${minsBefore} minutes!`,
            icon: "icon.png"
        });
    }
}

function scheduleNotifications(prayers){
    const selected = JSON.parse(localStorage.getItem('notifyPrayers') || "[]");
    const now = new Date();

    prayers.forEach(prayer => {
        if(selected.includes(prayer.name)){
            const iqamaDate = new Date(prayer.iqama);

            [10,5].forEach(minsBefore => {
                const notifyTime = iqamaDate.getTime() - minsBefore * 60000;
                const delay = notifyTime - now.getTime();
                if(delay > 0){
                    setTimeout(() => sendIqamaNotification(prayer.name, minsBefore), delay);
                }
            });
        }
    });
}
